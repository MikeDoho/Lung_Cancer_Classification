import torch

'''
To summarise, this loss function is weighted by the constants 'alpha' and 'beta' that penalise false positives and 
false negatives respectively to a higher degree in the loss function as their value is increased. The beta constant in 
particular has applications in situations where models can obtain misleadingly positive performance via highly 
conservative prediction. You may want to experiment with different values to find the optimum. With alpha==beta==0.5, 
this loss becomes equivalent to Dice Loss.
'''


class TverskyLoss(torch.nn.Module):
    def __init__(self, weight=None, size_average=True):
        super(TverskyLoss, self).__init__()

    def forward(self, inputs, targets, smooth=1, alpha=1, beta=0.5):
        # comment out if your model contains a sigmoid or equivalent activation layer
        # inputs = F.sigmoid(inputs)

        # flatten label and prediction tensors
        inputs = inputs.view(-1)
        targets = targets.view(-1)

        # True Positives, False Positives & False Negatives
        TP = (inputs * targets).sum()
        FP = ((1 - targets) * inputs).sum()
        FN = (targets * (1 - inputs)).sum()

        Tversky = (TP + smooth) / (TP + alpha * FP + beta * FN + smooth)

        return 1 - Tversky


